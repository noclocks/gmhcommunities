#' properties Shiny Module
#'
#' @name mod_properties
#'
#' @description
#' A Shiny Module for ...
#'
#' - `mod_properties_ui()`: User interface
#' - `mod_properties_server()`: Server logic
#'
#' @param id Module's namespace ID.
#'
#' @return
#' - `mod_properties_ui()`: UI HTML Output.
#' - `mod_properties_server()`: Reactive values returned from server logic.
#'
#' @examples
#' if (interactive()) {
#'   mod_properties_demo()
#' }
NULL


# UI ----------------------------------------------------------------------

#' @rdname mod_properties
#' @export
#' @importFrom shiny NS
#' @importFrom htmltools tagList tags
mod_properties_ui <- function(id) {

  ns <- shiny::NS(id)

  htmltools::tagList(
    bslib::card("PROPERTIES")
  )
}


# server ------------------------------------------------------------------

#' @rdname mod_properties
#' @export
#' @importFrom shiny moduleServer
#' @importFrom cli cat_rule
mod_properties_server <- function(id) {

  shiny::moduleServer(
    id,
    function(input, output, session) {

      ns <- session$ns

      cli::cat_rule("mod_properties_server")

      return(
        list(

        )
      )

    }
  )
}


# demo --------------------------------------------------------------------

#' @rdname mod_properties
#' @export
#' @importFrom pkgload load_all
#' @importFrom bslib page_fluid bs_theme
#' @importFrom shiny shinyApp
mod_properties_demo <- function() {

  pkgload::load_all()

  ui <- bslib::page_fluid(
    title = "Demo",
    theme = bslib::bs_theme(version = 5),
    lang = "en",
    mod_properties_ui("demo")
  )

  server <- function(input, output, session) {
    mod_properties_server("demo")
  }

  shiny::shinyApp(ui, server)
}
